int Maximal(int k){
    queue<pair<int, int> > pq;
    if(k>0)
        pq.push(make_pair(1, 1));
    int currLevel = 1;
    while(!pq.empty()){
        pair<int, int> top = pq.front();
        pq.pop();

        if(top.first != currLevel){
            make_changes(1);
            currLevel = top.first;
        }
        if(k>=1){
            if(tree[top.second].isPerfect){
                update(top.second);
                k--;
            }
        }
        for(int i=0; i<8 && (8 * top.second + i) <= maxLeaf; i++)
            if(leaves.find(8 * top.second + i) == leaves.end())
                pq.push(make_pair(top.first + 1, 8 * top.second + i));
    }
    make_changes(1);
    return tree[1].value;
}


